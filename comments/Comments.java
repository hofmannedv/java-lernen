// demonstrates how to comment your code
// usage: java Comments

public class Comments {
  public static void main (String[] args) {
    
    // comment on a single line
    int postcode;            // comment next to code

    // multi-line comments
    /* this is a comment
       that spans over
       three lines */
    boolean atHome = true;   // boolean variable pre-set with True
    boolean atWork = false;  // boolean variable pre-set with False
  }
}
